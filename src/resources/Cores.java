package resources;

import java.awt.Color;

public class Cores {
    private Color verdeEscuro;
    private Color verdeClaro;
    private Color preto;
    private Color branco;

    public Cores() {
        this.verdeEscuro = new Color(36,66,53);
        this.verdeClaro = new Color(113,136,120);
        this.branco = new Color(243,248,244);
        this.preto = new Color(17,20,23);
    }

    public Color getVerdeEscuro() {
        return verdeEscuro;
    }

    public Color getVerdeClaro() {
        return verdeClaro;
    }

    public Color getPreto() {
        return preto;
    }

    public Color getBranco() {
        return branco;
    }
    
    
}



